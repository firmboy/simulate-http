/**
 * @ClassName WebSocketConfig
 * @Description TODO
 * @Author playboy
 * @Date 2020/9/24 3:29 下午
 * @Version 1.0
 **/
@Configuration
public class WebSocketConfig {

    @Bean
    public ServerEndpointExporter serverEndpointExporter() {
        return new ServerEndpointExporter();
    }
}
